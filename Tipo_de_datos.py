#Tipo de datos entero o largo/long
print("Tipo de datos entero o largo/long")
numero = 15
print(numero,type(numero))
print()

#Tipo de dato flotante
print("Tipo de dato flotante")
numero_flotante= 15.5 
print(numero_flotante, type(numero_flotante) )
print()

#Tipo de dato número complejo
print("Tipo de dato número complejo")
numero_complejo = 5 + 6j
print(numero_complejo, type(numero_complejo) )
print()

#Tipo de dato string
print("Tipo de dato String")
nombre = "Charly"
print(nombre, type(nombre) )
print()

#Tipo dedato Booleano
verdadero_Falso = 3 == 3
print(verdadero_Falso, type(verdadero_Falso))
